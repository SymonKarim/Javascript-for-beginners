console.log('============ Join And Split method of Array ============');

// Join
const artist = ['Taylor', 'Bebe', 'Alan', 'Shawn', 'Harry'];
const join_artist = artist.join('->');
console.log(join_artist);


// Split
const message = 'This is me';
const split_msg = message.split(' ');
console.log(split_msg);


// join spliting message
const join_msg = split_msg.join('->');
console.log(join_msg);
