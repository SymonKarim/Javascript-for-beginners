console.log('============ Arithmatic Operators ============');

let x = 10;
let y = 5;

// Arithmatic
console.log(x + y);
console.log(x - y);
console.log(x * y);
console.log(x / y);
console.log(x % y);
console.log(x ** y);


// Increment
console.log(++x);
console.log(x++);
console.log(x);
console.log(++y);
console.log(y++);
console.log(y);


// Decrement
console.log(--x);
console.log(x--);
console.log(x);
console.log(--y);
console.log(y--);
console.log(y);