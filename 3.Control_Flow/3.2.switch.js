console.log('============ Switch Case ============');

let day = 5;

switch (day) {
    case 1:
        console.log('Saturday');
        break;
    case 2:
        console.log('Sunday');
        break;
    case 3:
        console.log('Monday');
        break;
    case 4:
        console.log('Tuesday');
        break;
    case 5:
        console.log('Wednesday');
        break;
    case 6:
        console.log('Thursday');
        break;
    default:
        console.log('Friday');
        break;
}